used_codes = set()

@bot.command()
async def redeem(ctx, code: str):

    code = code.strip().upper()
    print("CODE RECEIVED:", code)

    if code in used_codes:
        await ctx.send("❌ That code has already been used.")
        return

    if code in product1_codes:
        role = ctx.guild.get_role(ROLE_PRODUCT_1)

    elif code in product2_codes:
        role = ctx.guild.get_role(ROLE_PRODUCT_2)

    else:
        await ctx.send("❌ Invalid code.")
        return

    if role is None:
        await ctx.send("❌ Role not found.")
        return

    await ctx.author.add_roles(role)

    used_codes.add(code)

    await ctx.send(f"✅ You have been given the **{role.name}** role. Find products in the relevant channels!")

bot.run(TOKEN)
